// assignment
let a = 7;

console.log('before : ' + a);

// addition
console.log('addition : ', a += 5); // a = a + 5

// substraction
console.log('substraction : ', a -= 2); // a = a - 2

// multiplication
console.log('multiplication : ', a *= 3);  // a = a * 3

// division
console.log('division : ', a /= 2); // a = a / 2

// modulus
console.log('modulus : ', a %= 2); // a = a % 2  

// exponentiation
console.log('exponentation : ', a **= 2); // a = a**2

console.log('after : ' + a);