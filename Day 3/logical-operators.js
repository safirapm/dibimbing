// And
// kedua pembanding harus true jika mau mendapat nilai true, selain itu false
console.log("And Operator");
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
console.log("--------------\n");

// OR
// kedua pembanding harus bernilai false jika mau mendapat nilai false, selain itu true
console.log("Or Operator");
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);
console.log("--------------\n");

// NOT
// nilai kebalikan
console.log("Not Operator");
console.log(!true);
console.log(!false);
console.log("--------------\n");